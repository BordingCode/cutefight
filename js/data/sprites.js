let SPRITES = null;
export function buildSprites() {
  if (SPRITES) return SPRITES;
  SPRITES = {
    cinder: bakeSet({
      idle: [CINDER_IDLE_A, CINDER_IDLE_B],
      walk: [CINDER_WALK_A, CINDER_WALK_B],
      atk: [CINDER_ATK_A, CINDER_ATK_B],
    }, PAL),
    sproutle: bakeSet({
      idle: [SPROUTLE_IDLE_A, SPROUTLE_IDLE_B],
      walk: [SPROUTLE_WALK_A, SPROUTLE_WALK_B],
      tell: [SPROUTLE_TELL],
      lunge: [SPROUTLE_LUNGE],
    }, PAL),
    tiles: {
      grass: bake(TILE_GRASS, PAL),
      soil: bake(TILE_SOIL, PAL),
    },
    orb: bake(ORB, PAL),
  };
  return SPRITES;
}
